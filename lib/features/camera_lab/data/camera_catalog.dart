CameraRecipe _recipe({
  required String id,
  required String name,
  required String description,
  required CameraPack pack,
  required int seed,
  required String profile,
  double focalLength = 35,
  double exposure = 0,
  double contrast = 0,
  double saturation = 0,
  double warmth = 0,
  double grain = 0.08,
  double colorNoise = 0.04,
  double rolloff = 0.52,
  double distortion = 0,
  double edgeSoftness = 0.08,
  double aberration = 0.02,
  double vignette = 0.08,
  double bloom = 0.04,
  double halation = 0,
  double flash = 0,
  double underexposure = 0,
  double dust = 0,
  double scratches = 0,
  double lightLeak = 0,
  int deadPixels = 0,
  double compression = 0.02,
  double wear = 0.04,
  bool timestamp = false,
  ColorVector shadowTint = ColorVector.neutral,
  ColorVector highlightTint = ColorVector.neutral,
}) => CameraRecipe(
  schema: CameraRecipe.schemaV1,
  id: id,
  name: name,
  description: description,
  pack: pack,
  seed: seed,
  tags: [pack.serialName, profile],
  body: BodyProfile(
    profile: profile,
    dynamicRange: (0.62 - contrast * 0.28).clamp(0.12, 0.95),
    highlightRolloff: rolloff,
    baseNoise: colorNoise,
    saturationBias: saturation * 0.24,
  ),
  lens: LensProfile(
    profile: profile,
    focalLengthMm: focalLength,
    distortion: distortion,
    edgeSoftness: edgeSoftness,
    chromaticAberration: aberration,
    vignette: vignette,
    bloom: bloom,
    halation: halation,
  ),
  medium: MediumProfile(
    profile: profile,
    grain: grain,
    colorNoise: colorNoise,
    contrast: contrast,
    saturation: saturation,
    warmth: warmth,
    shadowTint: shadowTint,
    highlightTint: highlightTint,
  ),
  capture: CaptureProfile(
    exposureBias: exposure,
    whiteBalance: warmth * 0.22,
    flashStrength: flash,
    flashFalloff: flash == 0 ? 0 : 0.68,
    underexposure: underexposure,
    timestamp: timestamp,
  ),
  condition: ConditionProfile(
    dust: dust,
    scratches: scratches,
    lightLeak: lightLeak,
    deadPixels: deadPixels,
    compression: compression,
    wear: wear,
  ),
  protect: const [SemanticRegion.skin, SemanticRegion.text],
);
